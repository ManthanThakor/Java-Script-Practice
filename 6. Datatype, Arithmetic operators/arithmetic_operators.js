
/*In JavaScript, like in many programming languages, arithmetic operations are fundamental for mathematical computations. Here are the different types of arithmetic operations commonly used:*/

//1. Addition (+):

// Adds two numbers together or concatenates two strings.

let sum = 5 + 3; // Result: 8
let stringConcatenation = "Hello " + "world"; // Result: "Hello world"
console.log(sum);
console.log(stringConcatenation);


// 2. Subtraction (-):

// Subtracts the second number from the first.

let difference = 10 - 5; // Result: 5
console.log(difference);

// 3. Multiplication (*):

// Multiplies two numbers.

let product = 6 * 4; // Result: 24
console.log(product);

// 4. Division (/):

// Divides the first number by the second.

let quotient = 20 / 4; // Result: 5
console.log(quotient);


// 5. Remainder/Modulus (%):

// Returns the remainder of the division of the first number by the second.

let remainder = 10 % 3; // Result: 1 (because 10 divided by 3 is 3 with a remainder of 1)
console.log(remainder);

// 6. Exponentiation (** or Math.pow()):

// Raises the first number to the power of the second.

let result = 2 ** 3; // Result: 8 (2 raised to the power of 3)

// 7. Increment (++):

// Increases the value of a variable by 1.

let x = 5;
x++; // Equivalent to x = x + 1; Now x equals 6
console.log(x);
// 8. Decrement (--):

// Decreases the value of a variable by 1.

let y = 10;
y--; // Equivalent to y = y - 1; Now y equals 9
console.log(y);
