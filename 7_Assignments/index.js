
/*In JavaScript, assignment operators are used to assign values to variables. They combine the operation of assigning a value with another operation such as addition, subtraction, multiplication, etc. Here are the assignment operators in JavaScript:*/

// Assignment (=):

// Assigns a value to a variable.

let x = 5;

// Addition assignment (+=):

// Adds the value on the right to the current value of the variable and assigns the result to the variable.

let y = 10;
y += 5; // Equivalent to y = y + 5; Now y equals 15

// Subtraction assignment (-=):

// Subtracts the value on the right from the current value of the variable and assigns the result to the variable.

let z = 20;
z -= 7; // Equivalent to z = z - 7; Now z equals 13

// Multiplication assignment (*=):

// Multiplies the current value of the variable by the value on the right and assigns the result to the variable.

let a = 3;
a *= 4; // Equivalent to a = a * 4; Now a equals 12

// Division assignment (/=):

// Divides the current value of the variable by the value on the right and assigns the result to the variable.

let b = 50;
b /= 5; // Equivalent to b = b / 5; Now b equals 10

// Remainder assignment (%=):

// Computes the remainder of dividing the current value of the variable by the value on the right and assigns the result to the variable.

let c = 17;
c %= 5; // Equivalent to c = c % 5; Now c equals 2 (remainder of 17 divided by 5)

// Exponentiation assignment (**=):

// Raises the current value of the variable to the power of the value on the right and assigns the result to the variable.

let d = 2;
d **= 3; // Equivalent to d = d ** 3; Now d equals 8 (2 raised to the power of 3)



